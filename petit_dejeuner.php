                </span> <p> <span style="color: black;">Consommer des protéines au petit déjeuner permet de réduire la faim et l'appétit, de stimuler le métabolisme, de fournir de l'énergie, de limiter les envies de glucides et de maintenir la masse musculaire. Il s’agit donc d’une excellente stratégie pour rester en forme et pour encourager une bonne hygiène de vie tout au long de la journée.<br>
                    D’après une étude publiée par l'International Journal of Obesity et citée par Health, consommer des œufs au petit déjeuner par rapport à un bagel permettrait de diminuer le tour de taille de 34%. Une autre étude a prouvé l’efficacité d’une consommation élevée (30% de l’apport calorique quotidien) de protéines le matin sur la perte de poids à long terme. Voici quelques idées de petit déjeuners comportant au moins 20 grammes de protéines et moins de 400 kcal pour vous aider à stimuler votre métabolisme dès le matin.
                    </p>

                    <h1>Avoine, yaourt et fruits</h1>
                    <p>Le yaourt grec a un excellent rapport protéines/calories, ce qui en fait un allié de choix pour un petit déjeuner équilibré. L'avoine complète fournit jusqu'à 5 grammes de protéines par tasse, et les fibres contenues dans l'avoine facilitent la sensation de satiété. Mélangez du yaourt et des flocons d’avoine dans un bol avec des fruits frais ou congelés en morceaux, pour être rassasié jusqu’au déjeuner.</p>

                    <h1>Oeufs et légumes</h1> 
                    <img src="asset/images/oeuf&legume.jpg" alt="">
                    <p>Les œufs sont la référence en matière de protéines de qualité. Plusieurs études montrent qu'un petit déjeuner à base d'œufs aide à réduire les calories quotidiennes, à vous rassasier plus longtemps et à diminuer les envies de glucides. Mélangez un œuf entier ainsi que deux blancs d’œuf dans un bol, puis faites-les cuire à la poêle avec vos légumes préférés, comme des champignons, des oignons, des épinards ou des tomates. Dégustez le tout avec une tranche de pain complet.</p>
                 
                    <h1>Cottage et fruits</h1>
                    <p>Le fromage frais cottage, à base de lait caillé, est riche en leucine, un acide aminé qui aide à construire et à maintenir la masse musculaire. Le matin, préparez-vous un bol de ce fromage faible en matière grasse, et ajoutez-y des fruits frais comme de l’ananas ou des poires, puis garnissez l’ensemble avec des noix et noisettes hachées pour ajouter une touche croquante.</p>
                    
                    <h1>Smoothie chia</h1>
                    <p>Mélangez une tasse de lait végétal, une banane, une petite cuillerée de vanille ou de cacao en poudre, et une cuillerée à soupe de graines de chia. Mixez jusqu’à obtenir la consistance que vous préférez, puis dégustez.</p>
                   
