                <p> <span style="color: black;"> La pratique d'un sport s'apparente à une bonne alimentation équilibrée</p>
                
                <h1>Pourquoi ne perd-on pas de kilos en faisant plus de sport ?</h1>
                <p>SCHEIRLYNCK: le métabolisme s'adapte à l'activité de plus en plus élevée. En faisant du sport, le corps va brûler plus de calories et par conséquence, demander à s'alimenter davantage. Il faut pouvoir résister aux tentations et suivre un plan nutritionnel adapté pour éviter cela.</p>

                <h1>En théorie, on peut donc maigrir en faisant plus de sport?</h1>
                <p>Oui, mais ce n'est pas facile. La personne qui ne faisait jamais de sport auparavant va même prendre du poids au début de sa pratique sportive, car les muscles pèsent plus que la graisse. Je conseille donc aux débutants de porter leur attention sur un pourcentage de graisse sain et non sur une perte de poids.</p>
                
                <h1>Les sportifs aiment se récompenser après un effort. Comment leur éviter de se ruer sur des biscuits ?</h1>
                <p>Il faut effectivement manger un en-cas après avoir pratiqué un sport, car le corps a réalisé un effort physique et les muscles doivent se réparer. Les produits laitiers sont un bon choix, car ils aident les muscles à cicatriser tout en leur apportant du sucre. Il ne faut toutefois pas exagérer dans leur consommation : en mangeant 5 pots de yaourt, on reprend certainement plus que les calories brûlées en ayant fait du sport.</p>
                    
                    <h1>Si on ingurgite peu de calories après le sport, on peut donc perdre beaucoup de kilos?</h1>
                    <p>Oui, mais ce genre de régime ne nourrit pas un homme. Il y a des gens qui soudainement ne mangent que des soupes et des salades et qui combinent cela à un programme fitness qui leur fait dépasser leurs limites. Ils voient alors rapidement des résultats dans leur perte de poids, mais cela ne dure pas longtemps. Perdre du poids se fait idéalement de façon lente. En mangeant 200 calories en moins que ce dont le corps a besoin par jour. Cela représente seulement deux tartines. Ces efforts sont réalistes et font perdre du poids sur le long terme.</p>
                    
                    <h1>S'il ne fait pas maigrir, pourquoi encore faire du sport?</h1>
                    <p>Faire du sport est très sain, même si on ne maigrit pas. L'activité physique préserve des maladies cardiaques et améliore l'équilibre mental. Il n'y a pas de meilleur remède contre la dépression et le burn out que d'aller courir régulièrement. </p>
                    
