                <h1>Pratiquer un nouveau sport a des effets étonnants sur le cerveau</h1>
                <div class="info">
                    <p>Par <span class="blue">El Hadji Oumar Mbengue</span><span class="bold"> Il y'a 3 jours</span></p>
                    <p>Se lancer dans un nouveau sport au cours de sa vie, tels que la natation, le vélo ou encore le snowboard, peut renforcer et même modifier le cerveau, d'une manière différente que le feraient d'autres activités familières comme les mots-croisés ou les puzzles, selon une nouvelle étude rapportée par le New York Times.</p>
