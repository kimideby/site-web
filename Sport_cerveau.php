                <p>Quand nous pensons à l'apprentissage et à l'intelligence, la plupart d'entre nous pensent à des activités comme calculer, se souvenir de noms, écrire des poèmes ou apprendre un nouveau langage. De telles processus cérébraux complexes sont généralement classifiés d'"un ordre plus élevé" et sont le résultat d'activités dans certaines parties du cerveau qui encouragent la plasticité ou les changements physiques dans ces zones. Il a été prouvé qu'apprendre une nouvelle langue à l'âge adulte produit plus de matière blanche dans les parties du cerveau qui sont impliquées dans le processus du langage. <br>
                        Faire de l'exercice de façon régulière modifie le cerveau de la même manière. Ainsi, courir augmente le nombre de nouvelles cellules dans le cerveau créées dans les zones dédiés à la mémoire et à la réflexion. <br>
                        Des études menées dans le passé ont montré qu'apprendre une nouvelle activité physique à l'âge adulte augmente le volume de matière grise dans les zones du cerveau liées au contrôle des mouvements. <br>
                        En 2014, une étude menée sur des souris leur a fait utiliser un autre type de roue, plus compliquée à appréhender, dans laquelle les barreaux étaient espacés de manière irrégulière. Résultats : les souris étaient obligées d'apprendre un autre type de mouvement pour courir dans leur roue et la connexion des neurones dans leur cerveau en était fortement modifiée. Les autres souris qui continuaient à courir de manière normale dans des roues classiques n'expérimentaient pas cette modification cérébrale. <br>
                        </p>

                    <h1>Connexion plus rapide entre les neurones</h1>

                    
                    <p>Apprendre à utiliser ce nouveau type de roues a favorisé la myélinisation des neurones dans l'aire motrice des souris. La myélinisation est le processus de formation de la myéline, substance grasse qui forme la gaine de certaines fibres nerveuses, et qui permet aux connexions entre les neurones de se faire de façon souple et rapide. Par le passé, les scientifiques pensaient que ce processus dans le cerveau se réalisait exclusivement durant l'enfance et ralentissait par la suite. Les souris qui couraient dans les roues modifiées ont pourtant montré une amélioration dans le processus de myélinisation même en étant adultes. <br>
                        En d'autres mots, apprendre une nouvelle activité a permis de modifier le fonctionnement interne de l'air motrice des souris alors que pratiquer une activité déjà bien maîtrisée ne le faisait pas. "Nous ne pouvons pas affirmer que des modifications comparables se passent dans le cerveau de personnes adultes qui entament un nouveau sport ou activité physique mais il semblerait bien que ce soit le cas", déclare le docteur Krakauer. <br>
                        Commencer une nouvelle activité devrait donc avoir des effets salutaires sur le cerveau, et contrairement aux jeux vidéo, procurer, en plus de cela, tous les bénéfices physiques de l'exercice. Entamer ce genre de nouveau challenge à l'âge adulte permet de se rappeler qu'un corps peut toujours s'adapter et qu'il est avide de nouveaux mouvements. 
                        </p>

                    
