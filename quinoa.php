                </span> <p> <span style="color: black;">Le quinoa se cuisine comme une céréale, mais elle a d’autres atouts qui en font un aliment intéressant pour les végétariens, entre autres.<br>
                    Il est exempt de gluten et peut donc être consommé par les personnes intolérantes ou souffrant de la maladie cœliaque. Véronique Liégeois*,  diététicienne-nutritionniste, nous donne les trois bonnes raisons d’en manger. Le quinoa équilibre les repas des végétariens. Le quinoa fournit de nombreux   éléments utiles aux personnes qui boudent la viande   notamment. « Il apporte une quantité   intéressante de protéines (15 g aux 100 g), plus que dans les céréales comme le riz, le blé. Ces protéines permettent   de maintenir la masse musculaire, dit la diététicienne. « Le quinoa fournit aussi les huit acides aminés essentiels aux adultes, ce qui n’est pas le cas des céréales comme le riz, le maïs, le froment qui doivent être couplés avec des légumineuses. » <br>
                    Il apporte également 9 g de fer pour 100 g. « Bien que ce soit du fer non   héminique, moins bien absorbé par l’organisme que celui de la viande, précise la diététicienne, il est utile aux  globules rouges pour lutter contre l’anémie. » <br>
                    Pour favoriser son   absorption, il suffit de manger, au même repas, un aliment riche   en vitamine C comme les agrumes, le kiwi…
                    </p>

                    <h1>Le quinoa favorise une digestion légère</h1>
                    <p>Cette pseudo-céréale apporte 6 g de fibres aux 100 g, plus que le riz complet. « Ces dernières assurent un bon transit intestinal, explique la diététicienne. Elles offrent aussi un rapide sentiment de satiété, ce qui évite d’avoir envie de grignoter. »</p>

                    <h1>Le quinoa aide à contrôler son taux de cholestérol</h1> 
                    <p>Le quinoa est pauvre en lipides, il en renferme 1,2 g aux 100 g, et il est riche en fibres. Ces dernières sont utiles pour prévenir les maladies cardiovasculaires, car elles favorisent la régulation du taux de cholestérol en aidant à l’excrétion des graisses.  <br>
                        Le quinoa est énergétique, riche en potassium et en calcium <br>
                        •	Avec 380 kcal aux 100 g et 6,9 g de glucides complexes, le quinoa apporte autant d’énergie que le riz, les pâtes… Cette énergie est utile en hiver pour recharger les batteries. Son index glycémique est bas : 35, ce qui est intéressant. <br>
                        •	Le quinoa est riche en potassium. Il en apporte 740 mg aux 100 g, cela représente plus d’un tiers des besoins quotidiens en ce minéral indispensable pour réguler la pression artérielle et l’influx nerveux. <br>
                        •	Le quinoa fournit du calcium. Avec 60 mg aux 100 g, il comble entre 7 et 8 % des besoins quotidiens. C’est utile pour les personnes qui consomment peu ou pas de produits laitiers. <br>
                        </p>
                 
                   
