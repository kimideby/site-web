                <p> <span style="color: black;"> Certains jours, s'entraîner se révèle particulièrement pénible. On a l'impression d'avoir des jambes de cent kilos, on a du mal à reprendre son souffle et on éprouve l'envie irrésistible de ne plus jamais mettre un pied dans un club de sport. Cependant, il n'y a aucune raison pour que ces séances de sport - que ce soit du jogging, du fitness ou du vélo - soient aussi épuisantes. Voici quinze conseils pour se faciliter la vie.</p>
                
                <h1>Regardez droit devant vous</h1>
                <p>Quand on se promène, qu'on court ou qu'on fait du vélo, il peut être tentant de laisser errer son regard et de profiter de l'environnement. Cependant, une étude de 2014 de l'Université de New York révèle qu'il vaut mieux restreindre son regard. Les promeneurs qui regardaient droit devant eux pensaient que la ligne d'arrivée était plus proche de 28% et marchaient 23% plus vite que ceux qui regardaient autour d'eux. De plus, les participants qui ne laissaient pas errer leur regard indiquaient que l'activité leur paraissait plus facile.</p>

                <h1>Buvez du café</h1>
                <p>La caféine ne sert pas uniquement à se revigorer quand on a un petit coup de mou à 15 heures. Le magazine professionnel Appetite révèle que les cyclistes qui boivent une tasse de café une heure avant de partir trouvent leur promenade moins fatigante que les sportifs non remontés par la caféine.</p>
                
                <h1>Changez votre regard sur la douleur</h1>
                <p>No pain, no gain? Pas vraiment. Une étude de 2015 publiée dans Plos One affirme que les muscles fatigués sont moins douloureux quand on change son regard sur la douleur. Si on pense, par exemple, que "c'est la sensation d'un corps qui change", l'entraînement paraîtra moins douloureux que si on se dit "que c'est pénible".</p>
                    
                    <h1>Composez la playlist parfaite</h1>
                    <p>En 2015, une étude de l'Université canadienne McMaster révélait que les gens qui écoutent leur musique préférée en faisant du sport n'ont pas l'impression de s'épuiser. Cette illusion disparaissait quand ils devaient se passer de musique. Une étude de l'Université américaine Northwestern avait déjà démontré qu'à condition de comporter suffisamment de notes basses, la musique donne un sentiment de puissance et de contrôle.</p>
                    
                    <h1>Faites du sport en groupe</h1>
                    <p>À en croire une étude de l'Université d'Oxford, on est capable de supporter deux fois plus de douleur (et on produit deux fois plus d'endorphines) quand on fait du sport en groupe.</p>

                    <h1>Regardez-vous dans le miroir</h1>
                    <p>Il ne s'agit pas de vos tablettes de chocolat. Si vous vous regardez pendant une séance de home-trainer, vous conserverez un meilleur rythme et vous aurez un peu moins de mal que si vous portez votre regard sur autre chose.</p>

                    <h1>Consommez des betteraves</h1>
                    <p>Les betteraves contiennent des nitrates, qui aident à transporter plus de sang et d'oxygène vers les muscles. À en croire une étude britannique de l'Université d'Exeter, les gens qui boivent un demi-litre de jus de betterave deux heures et demie avant de faire du sport sont capables de courir ou de pédaler 15 à 20% plus avec moins d'oxygène.</p>

                    <h1>Rafraîchissez-vous avant de vous réchauffer</h1>
                    <p>Buvez une boisson glacée avant de faire du sport, surtout s'il fait chaud et humide. Vous verrez que vous tiendrez le coup plus longtemps. À en croire le magazine professionnel Journal of Medicine & Science in Sports, enfiler des sous-vêtements gelés aiderait également (même si cela ne nous paraît pas évident).</p>

                    <h1>Buvez une boisson énergétique</h1>
                    <p>Selon une étude parue dans Nutrients en 2015, les effets d'une boisson énergétique sont autant physiques que mentaux. Les glucides et les électrolytes procurent l'énergie nécessaire pour courir 16% de plus. Et à en croire une étude publiée dans la revue Psychology of Sport and Exercise, le simple fait de regarder une boisson énergétique ou une bouteille d'eau suffirait à tenir plus longtemps.</p>

                    <h1>Achetez un réveil luminescent</h1>
                    <p>Se réveiller avec une lumière vive ne permet pas uniquement de se lever plus facilement, mais stimule aussi l'activité physique. Une étude de 2014 de l'University College de Londres a prouvé que l'exposition à la lumière 30 minutes avant la sonnerie du réveil et à l'aube, stimule le transport d'oxygène vers les muscles.</p>

                    <h1>Vérifiez votre timing</h1>
                    <p>Certaines personnes sont du matin, d'autres pas du tout. Sachez qui vous êtes. Une étude de l'Université du Cap réalisée en 2014 révèle qu'il est plus facile de pratiquer un sport au moment de la journée où on se sent le plus en forme.</p>

                    <h1>Mangez un snack avant de faire du sport</h1>
                    <p>Si vous êtes du matin, ne sortez pas sans avoir mangé. Une étude de 2014 parue dans Sport Sciences for Health révèle en effet que faire l'impasse sur le petit-déjeuner rend la séance de sport beaucoup plus pénible.</p>

                    <h1>Prenez un bain chaud</h1>
                    <p>Oubliez les bains glacés. Non seulement un bain brûlant après chaque séance d'entraînement est beaucoup plus agréable, mais cette habitude facilite également la prochaine séance de sport.</p>

                    <h1>Vérifiez vos taux de fer</h1>
                    <p>D'après l'Université de Melbourne, les femmes qui souffrent d'un manque de fer et qui prennent des compléments font du sport plus efficacement et leur rythme cardiaque est plus lent.</p>

                    <h1>Améliorez votre qualité de sommeil</h1>
                    <p>Le sport fait mieux dormir. Mais l'inverse est vrai aussi : le sommeil facilite le sport. Si vous dormez mal, vous passerez moins de temps au club de fitness, explique une étude parue dans le Journal of Clinical Sleep Medicine. </p>
                   
