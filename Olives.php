                </span> <p> <span style="color: black;"> Vous aimez grignoter quelques olives entre les repas ou à l’apéritif ? Ça tombe bien, car ce fruit délicieux et parfumé, dont il existe près de 500 variétés différentes, est gorgé de nutriments bénéfiques pour la santé. L’intégrer à son alimentation quotidienne (sans en abuser) permet de booster le corps de la tête aux pieds.
                    Privilégiez les produits biologiques, cultivés par des agriculteurs locaux, au lieu des olives vendues en conserve par des producteurs à grande échelle, qui ont plus de risques de contenir des pesticides. Et profitez de leurs bienfaits :
                    </p>
                <h1>Pour la santé</h1>

                    <p>Manger des olives régulièrement aide à lutter contre l’inflammation car elles contiennent des polyphénols. Ce composé est connu pour ses pouvoirs antioxydants, anti-inflammatoires, antifongiques et antimicrobiennes. Il serait capable de diminuer le stress oxydatif dans les cellules du cerveau et d’iséliorer la mémoire, explique le docteur Franck Lipman sur le site iséricain Mind Body Green. Les acides gras monoinsaturés des olives aident à booster le bon cholestérol dans le sang ainsi qu’à réduire les risques de maladies cardiovasculaires et d’hypertension.
                    </p>

                    <h1>Pour les yeux et la peau</h1>

                    
                    <p>Si vous voulez protéger votre cornée, maintenir une bonne santé des yeux et lutter contre les rides, consommez des olives. Leur apport en vitamine A et E, des antioxydants essentiels, permet de lutter contre la dégénérescence maculaire, la cataracte et le glaucome, et aide à garder la souplesse et l’élasticité de la peau intacte.</p>

                    <h1>Pour la forme</h1>

                    <p>Malgré leur apport en graisses qui peut faire peur, les olives peuvent aider à garder la ligne. En effet, les acides gras monoinsaturés sont associés à une perte de la graisse abdominale, à une meilleure sensibilité à l’insuline, à une meilleure digestion et aident à ressentir la sensation de satiété plus facilement. Bonus : les olives permettent d’augmenter la production de glutathion dans le sang. Cette protéine est essentielle pour produire de l’énergie. Et les olives noires apportent une dose de fer qui va stimuler le système immunitaire.  </p>
                    
                    
