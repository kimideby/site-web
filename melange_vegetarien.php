                <br>
                </span> <p> <span style="color: black;"> Si vous avez décidé de renoncer à la viande, vous craignez peut-être de souffrir d’un manque de fer. En effet, de nombreux légumes contiennent ce minéral, mais notre corps a du mal à l’absorber. La bonne nouvelle est qu’il suffit d’associer ces aliments à de la vitamine C pour que l’organisme arrive à remplir ses réserves de fer. Au contraire, le thé, le café et les suppléments en calcium sont à éviter. Voici quelques idées de mélange d’aliments riches en fer et en vitamine C, à intervertir à volonté :</p>
                    <h1>Épinards et poivrons rouges</h1>
                    <p>Il existe une foule de possibilités pour cuisiner ces deux légumes, rappelle le site Health. Crus, en salade, rôtis, sautés à la poêle…vous pouvez même farcir un poivron avec des épinards ! Un fil d’huile d’olive et un peu d’ail frais ajoutent une touche parfumée idéale pour sublimer le goût de ces légumes.</p>

                    <h1>Brocolis et tomates</h1>
                    <p>Des brocolis cuits à la vapeur mélangés à un pesto de tomates séchées sont non seulement délicieux, mais aussi un excellent moyen pour augmenter son apport en fer. Le matin, vous pouvez les préparer en omelette après les avoir coupés en petits morceaux. Et si vous voulez faire le plein de légumes, vous pouvez faire sauter le brocoli pendant quelques minutes avec des tomates, des oignons, des champignons, de l’huile d’olive, de l’ail et des herbes de Provence. Servez ce mélange avec un peu de riz ou de quinoa, et vous aurez un repas parfaitement équilibré.</p>

                    <h1>Haricots noirs et chou</h1>
                    <p>Pour combiner ces deux produits, vous pouvez les préparer en salade, les faire revenir à la poêle, ou préparer des « maki » en utilisant les feuilles de chou pour faire un rouleau rempli d’haricots.</p>
                    
                    <h1>Lentilles et choux de Bruxelles</h1>
                    <p>Oubliez les préjugés et les traumatismes de la cantine. Grillées au four ou au barbecue, les choux de Bruxelles sont un excellent accompagnement pour les lentilles. Ajoutez quelques feuilles de salade et vous avez de quoi vous régaler.</p>
                    
                    <h1>Chocolat noir et fraises</h1>
                    <p>Cette version sucrée riche en fer et en vitamine C fonctionne avec n’importe quel fruit. Faites fondre le chocolat noir et trempez-y des quartiers d’orange, de morceaux de fraise ou des kiwi. Mixez-les pour obtenir un smoothie ou bien laissez-les quelques heures au congélateur pour que le chocolat durcisse, selon vos envies.</p>
                    
                    
