                <span><h2>NOS CONSEILS SANTÉ</h2></span> <p> <span style="color: black;"> Vaut-il mieux rester chez soi plutôt que d’aller courir le long des avenues polluées? </span><br>
                    Faire du sport est bon pour la santé, tout le monde en convient. Et tout le monde sait aussi que la pollution de l’air n’est pas sans effet sur l’organisme. Alors certains s’interrogent: Vaut-il mieux rester chez soi plutôt que d’aller courir le long des avenues polluées? Ou est-ce quand même bénéfique de faire du sport, même dans un environnement pollué?
                </p>
                <h1>Les risques à faire du sport dans une ville pollée</h1>

                    <p>«En cas d’effort intense, le débit ventilatoire, c’est-à-dire le volume d’air inspiré par unité de temps, peut être multiplié par dix», souligne le Dr Gilles Dixsaut, médecin et président du comité national contre les maladies respiratoires. Or bien souvent, cet air contient divers polluants, principalement issus du trafic routier, du chauffage urbain et de l’industrie: du dioxyde de soufre, du dioxyde d’azote, de l'ozone, mais aussi des particules fines.
                    <br> <br> 
                    «Plus les particules sont petites, plus elles pénètrent en profondeur dans notre organisme. Les PM 2,5 (diamètre inférieur à 2,5 micromètres) vont jusqu’aux alvéoles pulmonaires».
                    <br>
                    Dr Gilles Dixsaut, médecin et président du comité national contre les maladies respiratoires. <br>
                    Logiquement, les risques pour la santé liés à l’inhalation de particules augmentent donc en cas d’activité physique. Mais ces risques dépendent, entre autres, de la taille des particules. «Plus elles sont petites, plus elles pénètrent en profondeur dans notre organisme. Les PM 2,5 (particules dont le diamètre est inférieur à 2,5 micromètres, ndlr) vont jusqu’aux alvéoles pulmonaires», explique le Dr Dixsaut. Cancérogènes, irritantes… Leurs effets sont divers, et elles peuvent également favoriser les allergies au pollen, ou encore amplifier le caractère cancérigène d’autres substances, comme le tabac.

                    Plus petites encore, les particules ultrafines (moins de 0,1 micromètre de diamètre) peuvent passer dans la circulation sanguine et abîmer les artères. Le Pr Daniel Thomas, cardiologue et président d’honneur de la Fédération Française de Cardiologie, résume: «La pollution favorise le déclenchement d’événements graves - infarctus du myocarde, AVC, etc. - et a aussi des effets chroniques en altérant les parois des artères et faisant ainsi le lit de ces événements.»
                    </p>

                    <h1>Adapter son activité</h1>

                    <img src="asset/images/adapteActivité.jpg" alt="drone">
                    <p>Des risques avérés, mais a priori bien moins importants que ceux liés à l’alcool, au tabac, à l’obésité ou à l’exposition au soleil, d’après des données de Santé publique . Ainsi qu’aux problèmes qui découlent du manque d’activité physique. Selon l’Organisation mondiale de la Santé, ne pas faire de sport est l’un des 10 principaux facteurs de risque de mortalité mondiale.
                    </p>

                    <p>«La pollution, en tout cas hors des pics, est donc loin de contrecarrer tous les avantages du sport pour notre santé», affirme le cardiologue. AVC, diabète, cancers, hypertension artérielle: le sport permet dans une certaine mesure de limiter la survenue de nombreuses maladies. «Cela montre que même en cas de pollution, pratiquer une activité physique est primordial», poursuit-il.
                    </p>

                    <p>S’éloigner des grands axes routiers est également primordial. «Au milieu des voitures, les niveaux de pollution sont très élevés. Mais dès que l’on s’éloigne, elle diminue. À quelques mètres de la route, la pollution est déjà beaucoup plus faible», explique Charlotte Songeur, ingénieur à Airparif, l’association chargée de la surveillance de la qualité de l’air en Île-de-France.
                        Puisque cette diminution est significative dès les premiers mètres, emprunter les rues piétonnes ou rester dans un parc, même en plein centre-ville, fait déjà la différence. Des travaux menés par Airparif en 2008 ont ainsi montré que faire du vélo sur une piste cyclable permet déjà de réduire son exposition à la pollution. Un avantage d’autant plus marqué que la piste est éloignée de la route.
                        «Au milieu des voitures, les niveaux de pollution sont très élevés. Mais dès que l’on s’éloigne, elle diminue. À quelques mètres de la route, la pollution est déjà beaucoup plus faible».
                        </p>

                    

                    <h1>Faire attention à la météo</h1>

                    <p>Adapter son activité à la météo. «Mauvais temps ne veut pas dire pollution, au contraire!» prévient le Pr Thomas. Le vent, la pluie et la neige sont plutôt des alliés sur qui compter puisqu’ils permettent de balayer les particules. Mieux vaut donc privilégier les jours qui suivent un épisode pluvieux pour faire son footing. Au contraire, lorsqu’il fait beau et chaud, la concentration en ozone augmente. L’été n’est donc pas toujours la saison idéale pour faire du sport à l’extérieur.
                    </p>
                    <p>Il faut aussi se méfier du phénomène d’inversion des températures, accentué en automne, en hiver et au printemps. De quoi s’agit-il? «Lorsqu’il fait froid la nuit, le sol se refroidit très rapidement, et le matin, une masse d’air chaud peut bloquer au sol cet air froid, plus lourd», explique Charlotte Songeur. Or les polluants s’accumulent dans cet air non renouvelé.
                    </p>
                    <p>«Ce phénomène concerne surtout les quelques heures autour du lever du soleil, qu’il vaut donc mieux éviter», poursuit-elle. Ce moment de la journée correspond aussi aux premières heures de pointe du trafic routier. Deux bonnes raisons de courir plutôt en fin de journée que le matin.
                    </p>
                    <h1>Éviter le jogging en cas de pic de pollution</h1>
                    <img src="asset/images/sportMasquePollution.jpg" alt="drone">
                    <p>En cas de pic de pollution, les activités intenses sont déconseillées, notamment pour les enfants, les personnes âgées et celles atteintes de maladies cardiaques ou respiratoires. «Il est préférable de proscrire toutes les activités menant à une réelle augmentation du débit ventilatoire, estime le Pr Thomas. Mais il ne s’agit pas non plus de s’enfermer chez soi!», nuance le cardiologue. Des activités modérées restent toutefois possibles: on préférera par exemple la marche à la course à pied.
                    </p>
                    <p>Enfin, il faut éviter à tout prix les masques antipollution. «Ils bloquent les grosses particules, certes, mais pas les particules fines. Et surtout, ils empêchent de bien respirer, donc on hyperventile», insiste le cardiologue. Avec ces masques, les sportifs urbains respirent donc davantage de particules fines.
                    </p>
                    
                    
