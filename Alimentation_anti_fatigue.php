                 <p> <span style="color: black;"> Surmenage, stress, déséquilibres alimentaires... les raisons d’être fatigué ne manquent pas durant la saison froide... Pour déjouer les baisses d’énergie, voici les bases d’une alimentation antifatigue avec des nutriments indispensables au tonus.</span><br>
                </p>
                <h1>Ce qu'il faut manger pour lutter contre la fatigue</h1>

                    <p> •	Faire un petit déjeuner complet et dynamisant pour bien tenir jusqu’au déjeuner. <br>
                        •	Au déjeuner, intégrer une part de viande ou de poisson, riches en protéines stimulantes. <br>
                        
                        •	Prendre une collation dans l’après-midi si le dîner est éloigné du déjeuner et si vous ressentez une baisse d’énergie vers 17 heures. <br>
                        •	Prévoir du “cru” à chaque repas : 1 légume ou 1 fruit cru (ou 1 jus légumes/fruits) pour son apport en vitamine C, et du jus de citron à chaque fois que la recette s’y prête car il favorise l’assimilation du fer d’origine végétale. <br>
                        •	Intégrer à chaque repas 1 portion de céréales (pain, riz, pâtes semi-complètes...) ou de légumes secs pour leurs glucides lents diffusant de l’énergie sur la durée. <br>
                        •	Au dîner, plusieurs fois par semaine, associer les légumes secs aux céréales (riz, pâtes...) avec des légumes mais sans viande ou poisson. Exemples : curry de lentilles aux carottes servi avec du riz ; boulgour aux pois chiches et champignons... <br>
                        •	Agrémenter d’aromates (persil, menthe, thym, cumin...), de levure de bière ou de germes de blé ses préparations pour un complément en minéraux et antioxydants. <br>
                        •	Mettre une fois par semaine au menu abats, boudin, moules/fruits de mer ou viande rouge pour leur richesse en fer bien assimilé. <br>
                        •	Entre les repas, préférer l’eau plutôt que des cafés ou des boissons sucrées dont “l’effet coup de fouet” fatigue plus qu’il ne dynamise ! <br>
                        •	Faire le plein d’énergie sans abuser : pas de déjeuners trop copieux pour éviter la somnolence postprandiale. <br>
                        </p>




                    <h1>Le menu type anti-fatigue</h1>

                    <img src="asset/images/alimentationMenuType.jpg" alt="drone">
                    <p> •	Au petit déjeuner : boisson chaude. Pain aux céréales ou complet + beurre/margarine ou muesli (avec fruits séchés et/ou fruits oléagineux). Laitage. Fruit frais de saison. Facultatif : œuf ou jambon. <br>
                        •	Au déjeuner : 1 crudité assaisonnée. 1 part de viande ou poisson ou œufs. Légumes cuits. Céréales (pâtes, riz...) ou pain complet/aux céréales. 1 laitage et/ou 1 fruit (cru ou cuit selon entrée). <br>
                        •	Collation : 1 poignée de fruits oléagineux (noix, amandes, noisettes...). 1 fruit frais de saison ou banane. 1 boisson type rooibos ou de l’eau. <br>
                        •	Au dîner : 1 crudité ou 1 potage. Céréales (pâtes, riz...) + légumes secs avec plus ou moins de légumes (selon entrée). Fruit frais ou cuit.
                        </p>




                    <h1>Menus anti-fatigue, jour 1</h1>
                    

                    <p>  Petit déjeuner : <br>
                        •	Thé vert aux agrumes<br>
                        •	Muesli aux raisins secs, noisettes + yaourt + rondelles de banane<br><br>
                        Déjeuner<br>
                        •	Blanc de poulet vapeur aux brocolis (sauce : jus de citron vert, sauce soja, gingembre frais haché, huile de sésame, coriandre)<br>
                        •	Mimolette + pain complet<br>
                        •	Quart d’ananas<br><br>
                        Collation<br>
                        •	1 poignée d’amandes<br>
                        •	2 clémentines<br><br>
                        Dîner<br>
                        •	Salade d’avocat, mâche, carottes râpées, pomelo, riz semi-complet + vinaigrette<br>
                        •	Papillote de poire à la badiane<br><br>
                        </p>
                    
