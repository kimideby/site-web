                  <h1>Quels sont les effets de la course sur le corps humain?</h1>

                    
                    <p>Il est estimé que 51% des adultes aux Etats-Unis et 31% dans le monde ne font pas le minimum d'exercice recommandé pour rester en bonne santé, soulignent les chercheurs de l'Institut national américain du cancer (NCI), dont l'étude paraît en ligne dans le Journal of the American Medical Association, Internal Medicine. <br>
                        Il s'agit de marcher, de courir, de nager ou encore de faire du vélo à un rythme allant de modéré à vigoureux pendant 150 minutes par semaine, précisent les chercheurs. <br>
                        Des centaines d'études faites précédemment ont analysé le lien entre activité physique et réduction du risque de développer un cancer du colon, du sein et de l'endomètre, tissu tapissant l'utérus. <br>
                        Mais les résultats n'ont pas été concluants pour les autres types de tumeurs en raison du trop petit nombre de participants à ces travaux, notent les auteurs de la nouvelle étude. <br>
                        Ces derniers ont eux travaillé sur des données provenant de 1,44 million de personnes âgées de 19 à 98 ans aux Etats-Unis et en Europe. Les participants ont été suivis pendant onze ans en moyenne, une période durant laquelle 187.000 nouveaux cas de cancer ont été diagnostiqués. <br>
                        Leur étude a non seulement confirmé le lien déjà mis en évidence entre un degré soutenu d'exercice et la plus forte baisse du risque de cancer colorectal, du sein et de l'endomètre mais aussi avec dix autres tumeurs. <br>
                        Les chercheurs ont ainsi détecté une réduction du risque pour les cancers suivants: oesophage (-42%), foie (-27%), poumon (-26%), rein (-23%), estomac (-22%), endomètre (-21%), sang (-20%), colon (-16%) et sein (-10%).<br>
                        Dans la plupart des cas, le lien entre activité physique et baisse du risque de cancer a subsisté quelque soit le poids de la personne et qu'il s'agisse d'un fumeur ou non. Pour l'ensemble des cancers, la baisse du risque résultant d'un exercice régulier vigoureux a été de 7%.<br>
                        En revanche, les activités physiques ont été liées à un accroissement de 5% du risque de cancer de la prostate et de 27% du mélanome, un cancer agressif de la peau, surtout dans des régions très ensoleillées aux Etats-Unis. <br>
                        "Nos résultats montrent que le lien entre exercice et réduction du risque de cancer peut être généralisé à différents groupes de population y compris parmi les personnes en surpoids et obèses et celles qui ont fumé", souligne le Dr Steven Moore, chercheur du National Cancer Institute et principal auteur. <br>
